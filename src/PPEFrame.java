    private void updateTransactionHospitalButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateTransactionHospitalButtonActionPerformed
        // TODO add your handling code here:
        String transactionid = transactionid1.getText();
        String newitemcode = searchItemTextField1.getText();
        String newitemname = searchItemnameTextField1.getText();
        String newquantity = quantityDistributeTextField.getText();
        String newtransaction_status = hospitalpending1.isSelected() ? "Pending" : "Completed";
        String newparty = hospitalCodeCombobox1.getSelectedItem().toString();

        String formattedItemCode = newitemcode.toUpperCase();

        if (transactionid.isBlank()) {
            JOptionPane.showMessageDialog(null,
                    "Please enter a new Transaction ID.",
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
            return;
        }

        try {
            int quantity = Integer.parseInt(newquantity);
            // Check available stock before proceeding with the transaction
            PPE ppe_item = ppeManagement.searchItemByCode(formattedItemCode);

            if (ppe_item != null) {
                if (ppe_item.getQuantity() < quantity) {
                    JOptionPane.showMessageDialog(null,
                            "Insufficient stock. Current stock for " + formattedItemCode + " is: " + ppe_item.getQuantity() + " Please retry.",
                            "Stock Error",
                            JOptionPane.ERROR_MESSAGE);
                    return; // Stop the transaction if insufficient stock
                }
            } else {
                JOptionPane.showMessageDialog(null,
                        "Error while processing stock for the item code: " + formattedItemCode,
                        "Stock Error",
                        JOptionPane.ERROR_MESSAGE);
                return; // Stop the transaction due to unexpected error
            }

            //proceed with transaction if stock is sufficient
            String newprice_per_box = priceperboxTextField2.getText();

            if (!newprice_per_box.matches("\\d+\\.\\d{2}?")) {
                throw new NumberFormatException("Invalid price format.");
            }

            BigDecimal price_per_box = new BigDecimal(newprice_per_box).setScale(2, RoundingMode.HALF_UP);
            BigDecimal newtotal_price = price_per_box.multiply(BigDecimal.valueOf(quantity));

            String newtransaction_type = "Distributed";
            LocalDateTime newtransaction_date_time = LocalDateTime.now();

            boolean isAdded = ppeManagement.addTransaction(transactionid, formattedItemCode, newitemname, newparty, newtransaction_status, quantity, newtransaction_type, newtransaction_date_time, price_per_box, newtotal_price);
            if (isAdded) {
                JOptionPane.showMessageDialog(this,
                        "Transaction added successfully.",
                        "Success", JOptionPane.INFORMATION_MESSAGE);
                updatePPESearchTable(ppeManagement.getTransaction());
                ppeManagement.distributeStockToHospital(newitemcode, quantity); // Reduce stock after successful transaction
                loadPPEDataToTable();
                loadTransactionDataToTable();
            } else {
                JOptionPane.showMessageDialog(this,
                        "Failed to add the transaction.",
                        "Error",
                        JOptionPane.ERROR_MESSAGE);
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Please enter valid values: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "An unexpected error occured: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_updateTransactionHospitalButtonActionPerformed

