    private void loginButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginButtonActionPerformed
        // TODO add your handling code here:
        String uid = user_id.getText();
        char[] pwd = password.getPassword();

        // Convert char to string
        String pass = new String(pwd);
        User loggedInUser = userManagement.login(uid, pass);

        // Check if user is found and password matches
        if (loggedInUser != null) {

            // Use polymorphism to get the welcome message
            String message = loggedInUser.getWelcomeMessage();
            // Show welcome message
            JOptionPane.showMessageDialog(this, message);

            user_id.setText("");
            password.setText("");
            // Update button visibility based on the logged-in user
            updateButtonVisibility(loggedInUser);

            CardLayout cardLayout = (CardLayout) mainPanel.getLayout();
            cardLayout.show(mainPanel, "dashboardPanel");
            CardLayout cardLayoutContent = (CardLayout) Content.getLayout();
            cardLayoutContent.show(Content, "dashboardContent");
        } else {
            JOptionPane.showMessageDialog(this, "Invalid login details.");
        }


    }//GEN-LAST:event_loginButtonActionPerformed

