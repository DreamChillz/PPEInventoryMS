    private void loadUserDataToTable() {
        DefaultTableModel model = (DefaultTableModel) userTable.getModel();

        // Clear existing rows in the table
        model.setRowCount(0);
        List<User> users = userManagement.getUsers();

        for (User user : users) {
            // Determine the user type based on the instance type
            String userType;
            if (user instanceof AdminUser) {
                userType = "Admin";
            } else if (user instanceof StaffUser) {
                userType = "Staff";
            } else {
                userType = "Unknown"; // In case there's an unexpected type
            }

            // Prepare row data for the table
            Object[] rowData = {
                user.getUser_ID(),
                user.getName(),
                user.getPassword(),
                userType, // Use the determined userType
                user.getEmail(),
                user.getPhone_no()
            };

            // Add the row to the table
            model.addRow(rowData);
        }
    }

