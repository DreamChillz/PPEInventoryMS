    private void deleteTransactionSubmitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteTransactionSubmitButtonActionPerformed
        String transactionidDelete = deleteTransactionTextField.getText().trim();

        int confirmation = JOptionPane.showConfirmDialog(this,
                "Are you sure you want to delete this transaction?",
                "Delete Confirmation",
                JOptionPane.YES_NO_OPTION,
                JOptionPane.WARNING_MESSAGE);

        if (confirmation == JOptionPane.YES_OPTION) {
            // Proceed with deletion
            ppeManagement.deleteTransaction(transactionidDelete);
            JOptionPane.showMessageDialog(this,
                    "Transaction has been successfully deleted.",
                    "Success",
                    JOptionPane.INFORMATION_MESSAGE);

            // Clear input fields
            deleteTransactionTextField.setText("");
            deleteTransactionSubmitButton.setEnabled(false);

            try {
                // Refresh table
                loadTransactionDataToTable();
                updatePPESearchTable(ppeManagement.getTransaction());
            } catch (IOException ex) {
                Logger.getLogger(PPEFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            // Deletion cancelled, do nothing
            JOptionPane.showMessageDialog(this,
                    "Transaction deletion cancelled.",
                    "Cancelled",
                    JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_deleteTransactionSubmitButtonActionPerformed

