    private void submitModifyUserButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitModifyUserButtonActionPerformed
        // TODO add your handling code here:
        String currentuserid = userid1.getText().trim();
        String newname = name1.getText().trim();
        String newusertype;
        if (admin1.isSelected()) {
            newusertype = "admin";
        } else {
            newusertype = "staff";
        }

        String newemail = email1.getText().trim();
        String newphoneno = phoneNo1.getText().trim();

        // Validation: check for empty fields
        if (currentuserid.isEmpty()) {
            JOptionPane.showMessageDialog(this,
                    "Please enter a valid User ID.",
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
            return;
        } else if (newname.isEmpty() || newusertype.isEmpty()
                || newemail.isEmpty() || newphoneno.isEmpty()) {
            JOptionPane.showMessageDialog(this,
                    "Please ensure all fields are filled out.",
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
            return;

        }

        boolean modified = userManagement.modifyUser(currentuserid, newname, newusertype, newemail, newphoneno);

        if (modified) {
            // Show success message
            JOptionPane.showMessageDialog(this,
                    "User details have been successfully updated!",
                    "Success",
                    JOptionPane.INFORMATION_MESSAGE);

            // Clear input fields
            userid1.setText("");
            name1.setText("");
            email1.setText("");
            phoneNo1.setText("");
            userTypeButtonGroup1.clearSelection();

            // Update the latest array to table
            loadUserDataToTable();
        } else {
            // If the modification fails 
            JOptionPane.showMessageDialog(this,
                    "Modification failed.",
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_submitModifyUserButtonActionPerformed

